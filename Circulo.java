/*
 * Clase para obtener resultados de de un circulo.
 */
public class Circulo {


 //Método que suma dos números enteros y devuelve el resultado.
 public double area (double radio) {

	double result = 0;
	result = 3.1416*radio*radio;
	return result;
 }



 //Método que calcula la circunferencia
 public double circunferencia (double radio) {

	double result = 0;
	result = 2*3.1416*radio;
	return result;
 }

}
